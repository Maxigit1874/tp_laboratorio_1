
/** \brief Suma 2 enteros y devuelve el resultado.
 *
 * \param Primer numero entero a sumar.
 * \param Segundo numero entero a sumar.
 * \return Resultado de la suma de a + b.
 *
 */

int suma (int a, int b);




/** \brief Resta 2 enteros y devuelve el resultado.
 *
 * \param Primer numero entero a restar.
 * \param Segundo numero entero a restar.
 * \return Resultado de la resta de a - b.
 *
 */

int resta (int a, int b);




/** \brief Multiplica 2 enteros y devuelve el resultado.
 *
 * \param Primer numero entero a multiplicar.
 * \param Segundo numero entero a multiplicar.
 * \return Resultado de la multiplicacion de a * b.
 *
 */

int multiplicar (int a, int b);




/** \brief Divide 2 con coma y devuelve el resultado.
 *
 * \param Primer numero float a dividir.
 * \param Segundo numero float a dividir.
 * \return Resultado de la division de a / b.
 *
 */

float dividir (float a, float b);




/** \brief Calcula el factorial de un numero entero.
 *
 * \param Recibe solo un numero entero.
 * \return Resultado del factorial del entero.
 *
 */

long long factorizar (int a);
